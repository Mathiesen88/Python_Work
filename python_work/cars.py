cars = ['bmw', 'audi', 'toyota', 'subaru']
print(cars)

print(sorted(cars)) # sorts the list temporrily

cars.reverse() # reverses the order of the list
print(cars)

len(cars) # returns the number of elements in the list
print(len(cars))

locations = ['paris','london','new york','tokyo','rome']
print(locations)

print(sorted(locations)) # sorts the list temporarily
print(locations)

print(sorted(locations, reverse=True)) # sorts the list temporarily in reverse order
print(locations)

locations.reverse() # reverses the order of the list
print(locations)
locations.reverse() # reverses the order of the list agains
print(locations)